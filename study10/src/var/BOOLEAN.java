package var;

public class BOOLEAN {
	public static void main (String []args) {
	//boolean:불리언	
	//true(참) 또는 false(거짓)만을 저장할 수 있는 작은 변수타입
	//변수제작법
	//타입 이름;
	boolean a;
	//boolean true; 이름에는 예약어를 사용할 수 없다.//원칙
	a=true;
	System.out.println(a);
	
	boolean b=false;//간단
	System.out.println(b);
	
	boolean c=false,d=true;//, 쉼표는 구별을 의미
	System.out.println(c);
	System.out.println(d);
	
	//이름이 f인 불리언 타입에 변수를 만들어서 false를 입력하고
	//콘솔에 출력하시오
	boolean f;
	f=false;
	System.out.println(f);
	
	boolean t;
	t=false;
	System.out.println(t);
	
	} 
}
