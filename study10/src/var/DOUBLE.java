package var;

public class DOUBLE {
	public static void main (String [] args) {
		//부동소수점을 표현할 때 사용하는 타입
		//float:짧은 소수점을 표현할 때 사용하는 타입
		//double:긴 소수점을 표현할 때 사용하는 타입*X5,000,000 (정밀하게 표현)
		float a;
		a=3.123456f;
		System.out.println(a);
		double b;
		b=3.789456789635782d;
		System.out.println(b);
	}
}
