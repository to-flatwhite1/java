package cond;

public class WHILE {
	public static void main(String [] args) {
		//읽기 쉬운 코드를 적어라
		//반복문 while
		//while(조건){반복 수행할 문장;} 거짓이면 실행이 안됨
		
		//반복문 do-while
		//조건이 거짓일지라도 한번은 무조건 반복하도록 하는 문법
		int i=5;
		do {
			i++;
			System.out.println("반복중");
			}while(i>10);
		
	}//main 끝
}//class 끝
