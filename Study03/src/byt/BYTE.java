package byt;

public class BYTE {
	public static void main(String[] args) {
		//byte :바이트
		//아주 작은 수를 저장 할 수 있는 규격 -128 ~ 127
		//통상적인 프로그램 제작에는 사용 할 수 없다.
		
		byte a=-100;
		System.out.println(a);
		//바이트는 잘 쓰지 않아요.
		
		//short:숏
		//작은 수를 저장 할 수 있는 규격 -32768~32767
		//숏도 잘 쓰지 않아요
		short b=1000;
		System.out.println(b);
		
}//class 끝
}//main 끝
	
