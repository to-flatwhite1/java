package var;

public class Start {
	public static void main(String[] args) {
		//변수 만드는 법
		//크기 이름;
		int a;
		a=1000;//a상자에 1000을 넣는다
		a=90000;//변수 상자 내용물은 하나에 하나만 들어간다.
		System.out.println(a);//a 상자의 내용물을 적어주세요
		//변수는 한번에 하나의 데이터만을 가질 수 있다.
		
		//변수의 타입 종류 (상자의 크기 및 규격)
		boolean b;
		char c;
		byte B;
		short s;
		int i;
		long L;
		float f;
		double d;
	
	}//main 끝
}//class 끝
