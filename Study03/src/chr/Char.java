package chr;

public class Char {
	public static void main(String[] args) {
		//char:케릭터
		//문자 하나를 저장할 수 있는 규격
		//문자 하나를 쓸 때는 앞 뒤에 '를 붙인다 '가'
		//케릭터 타입에는 ""데이터를 넣을 수 없다.
		//''내부에는 여러개의 문자가 들어 갈 수 없다.
		
		//불편해서 잘 사용하지 않는다.
		char b;
		b='안';
		System.out.print(b);
		b='녕';
		System.out.print(b);
		b='하';
		System.out.print(b);
		b='세';
		System.out.print(b);
		b='요';
		System.out.print(b);
		b='.';
		System.out.print(b);
}//main 끝
	
}//class끝	
