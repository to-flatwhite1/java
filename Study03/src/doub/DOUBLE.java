package doub;

public class DOUBLE {
	public static void main(String[] args) {
		//float : 플로트
		//짧은 소수점을 표기 할 수 있는 규격
		//통상적으로 소수점 이하 7자리까지 포함가능
		//짧은 소수점을 표현할때는 f를 붙여서 표기한다.
		float b=0.123f;
		
		//double : 더블
		//긴 소수점을 표기할 수 있는 규격
		//*X5,000,000 일단 소수점을 적으면 double로 본다.
		//통상적으로 소수점 이하 15자리까지 포함 가능
		//double은 d를 붙여도 되고 안붙여도 된다.
		double a=0.123456789d;
	}
}
